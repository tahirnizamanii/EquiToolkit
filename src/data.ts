export function diagnoseSaddleFit(answers: { [key: string]: string }): string[] {
  const issues: string[] = [];

  if (answers.behavior === 'sore_back') {
    issues.push(
      '⚠️ Girth and Back Soreness: Your horse\'s behavioral defense triggers strongly suggest physical pain from tree pinching, bridging pressure, or spinal impingement. Immediate inspection of the panels is recommended.'
    );
  }

  if (answers.dry_spots === 'pressure_points') {
    issues.push(
      '⚠️ Localized Pressure Points: Dry patches inside wet zones indicate excessive focal pressure, which squeezes out sweat glands. This often means the tree is too narrow, pinching the shoulders, or has uneven flocking.'
    );
  } else if (answers.dry_spots === 'bridging') {
    issues.push(
      '⚠️ Saddle Bridging: Dryness in the center of the panels means the saddle only contacts at the front and back, like a bridge. This concentrates the rider\'s weight onto the horse\'s shoulders and loins, causing severe soreness.'
    );
  }

  if (answers.movement === 'pommel_low') {
    issues.push(
      '⚠️ Rocking/Balance Issue (Pommel Low): If the cantle lifts or bounces, the saddle is pivoting on the shoulders. The tree may be too wide, causing the pommel to drop forward, or the saddle\'s longitudinal curve doesn\'t match your horse\'s back.'
    );
  } else if (answers.movement === 'wide_or_flat') {
    issues.push(
      '⚠️ Shoulder Slips: A saddle sliding forward onto the shoulder blades locks the scapula, limiting gait. This is common in round, flat-backed horses or when the gullet is too wide.'
    );
  } else if (answers.movement === 'asymmetry') {
    issues.push(
      '⚠️ Lateral Slipping: Slipping to one side can indicate horse back asymmetry, rider weight imbalance, or uneven saddle flocking. It stretches the spine laterally and should be analyzed by a professional.'
    );
  }

  if (answers.clearance === 'narrow_or_sagging') {
    issues.push(
      '⚠️ Withers Clearance Critical: Under-rider clearance of less than 2 fingers (or touching) can cause catastrophic spinal bruising. The gullet tree is likely too wide or the flocking has compressed completely.'
    );
  } else if (answers.clearance === 'too_narrow') {
    issues.push(
      '⚠️ Tree Too Narrow: A high-riding pommel suggests the gullet angle is too narrow, sitting on top of the horse\'s back like a wedge rather than wrapping it. This will pinch the shoulder muscles.'
    );
  }

  if (issues.length === 0) {
    issues.push(
      '✅ Excellent Fit Indicators: Based on your responses, the saddle is balanced, stable, clearances are correct under load, and the horse shows zero behavioral discomfort. Keep monitoring sweat patterns regularly!'
    );
  }

  return issues;
}

