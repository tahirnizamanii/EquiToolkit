// FOAL COLOR GENETICS SIMPLIFIED DATA
export interface FoalProbability {
  color: string;
  probability: number; // percentage
}

export const HORSE_COLORS = [
  'Chestnut', 'Black', 'Bay', 'Grey', 'Palomino', 'Buckskin', 'Dun', 'Roan', 'Cremello', 'Perlino'
];

export function predictFoalColor(sire: string, dam: string): FoalProbability[] {
  // If either parent is Grey, there is a strong chance of Grey
  const greyCount = (sire === 'Grey' ? 1 : 0) + (dam === 'Grey' ? 1 : 0);
  
  // Recessive Chesnut check
  if (sire === 'Chestnut' && dam === 'Chestnut') {
    return [{ color: 'Chestnut', probability: 100 }];
  }

  // Double Dilute outcomes
  if (sire === 'Cremello' && dam === 'Cremello') {
    return [{ color: 'Cremello', probability: 100 }];
  }
  if ((sire === 'Cremello' && dam === 'Chestnut') || (sire === 'Chestnut' && dam === 'Cremello')) {
    return [{ color: 'Palomino', probability: 100 }];
  }
  if ((sire === 'Cremello' && dam === 'Black') || (sire === 'Black' && dam === 'Cremello')) {
    return [
      { color: 'Buckskin', probability: 75 },
      { color: 'Smoky Black', probability: 25 }
    ];
  }

  // Base probabilities lookup dictionary
  const baseCombos: { [key: string]: FoalProbability[] } = {
    'Chestnut_Bay': [
      { color: 'Bay', probability: 45 },
      { color: 'Chestnut', probability: 40 },
      { color: 'Black', probability: 15 }
    ],
    'Bay_Bay': [
      { color: 'Bay', probability: 65 },
      { color: 'Chestnut', probability: 25 },
      { color: 'Black', probability: 10 }
    ],
    'Black_Black': [
      { color: 'Black', probability: 75 },
      { color: 'Chestnut', probability: 25 }
    ],
    'Black_Bay': [
      { color: 'Bay', probability: 50 },
      { color: 'Black', probability: 30 },
      { color: 'Chestnut', probability: 20 }
    ],
    'Chestnut_Black': [
      { color: 'Bay', probability: 40 },
      { color: 'Chestnut', probability: 35 },
      { color: 'Black', probability: 25 }
    ],
    'Palomino_Palomino': [
      { color: 'Palomino', probability: 50 },
      { color: 'Chestnut', probability: 25 },
      { color: 'Cremello', probability: 25 }
    ],
    'Palomino_Chestnut': [
      { color: 'Palomino', probability: 50 },
      { color: 'Chestnut', probability: 50 }
    ],
    'Buckskin_Bay': [
      { color: 'Bay', probability: 40 },
      { color: 'Buckskin', probability: 40 },
      { color: 'Chestnut', probability: 10 },
      { color: 'Palomino', probability: 10 }
    ]
  };

  const key1 = `${sire}_${dam}`;
  const key2 = `${dam}_${sire}`;
  let baseResults: FoalProbability[] = baseCombos[key1] || baseCombos[key2] || [
    { color: 'Bay', probability: 40 },
    { color: 'Chestnut', probability: 40 },
    { color: 'Black', probability: 20 }
  ];

  // Modify base results if Grey is involved
  if (greyCount === 2) {
    // 75% Grey, remaining 25% are base colors
    return [
      { color: 'Grey', probability: 75 },
      ...baseResults.map(p => ({ color: p.color, probability: Math.round(p.probability * 0.25) }))
    ];
  } else if (greyCount === 1) {
    // 50% Grey, remaining 50% are base colors
    return [
      { color: 'Grey', probability: 50 },
      ...baseResults.map(p => ({ color: p.color, probability: Math.round(p.probability * 0.5) }))
    ];
  }

  // Dilute modifiers for Palomino, Buckskin, Dun, Roan, etc.
  const roanCount = (sire === 'Roan' ? 1 : 0) + (dam === 'Roan' ? 1 : 0);
  if (roanCount > 0) {
    const roanProb = roanCount === 2 ? 75 : 50;
    return [
      { color: 'Roan', probability: roanProb },
      ...baseResults.map(p => ({ color: p.color, probability: Math.round(p.probability * ((100 - roanProb) / 100)) }))
    ];
  }

  const dunCount = (sire === 'Dun' ? 1 : 0) + (dam === 'Dun' ? 1 : 0);
  if (dunCount > 0) {
    const dunProb = dunCount === 2 ? 75 : 50;
    return [
      { color: 'Dun (Zebra Dun)', probability: dunProb },
      { color: 'Red Dun', probability: Math.round(dunProb * 0.3) },
      ...baseResults.slice(0, 2).map(p => ({ color: p.color, probability: Math.round(p.probability * ((100 - dunProb * 1.3) / 100)) }))
    ].filter(p => p.probability > 0);
  }

  return baseResults;
}

